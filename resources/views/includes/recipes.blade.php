    <b>Instructions</b>
    <ul>
        <li>Remove the bones of the smoked titus fish and shred into small pieces.</li>
        <li>Roughly blend tomatoes, pepper and scotch bonnet</li>
        <li>Chop the onions and set aside</li>
        <li>Chop enough veggies to measure 2 to 3 cups. If using frozen veggies, simply measure 3 cups.</li>
        <li>Heat 30mls (two tablespoons) of cooking oil, in a non-stick pan and add the chopped onions. Heat till onions are transparent looking.</li>
        <li>Add the roughly blended pepper, and heat for about a minute on low to medium fire.</li>
        <li>Crush in a seasoning cube, add the curry, thyme and garlic powder</li>
        <li>Stir well, and allow to heat for another 2 minutes.</li>
        <li>Now, pour in fish and stir in</li>
        <li>Add all the chopped vegetables and sweet corn, stir-well, then allow to cook on low heat for about 5 minutes.</li>
        <li>Split content of the pan into 3 equal portions and store in your fridge</li>
        <li>Each portion is one serving</li>
