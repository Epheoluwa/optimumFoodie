                        <h3>Snack Options</h3>
                        <p>You can pick any of the following options:</p>
                        <ul>
                            <li>½ cup of oats (measure raw then cook) + ½ cup of skimmed milk OR</li>
                            <li>250mls of smoothie OR</li>
                            <li>2/3 cup Greek yoghurt + 1 small banana (or 1 cup halved strawberries) OR</li>
                            <li>2/3 cup of Greek yoghurt +  10 peanuts (or 6 cashew nuts) OR</li>
                            <li>5 pieces of Jacob’s crackers OR</li>
                            <li>3 cups of popcorn (see recipe) OR</li>
                            <li>1 cup of soy milk + 2 pieces of crackers OR</li>
                            <li>3 servings of fruit (see what one fruit means in the index above). Eat them whole or blend as a smoothie with some cucumbers. Here are some examples: 1 cup of halved strawberries + 2 medium bananas </li>
                            <li>1 cup of halved strawberries + 1/2 cup of blueberries + 1 small apple OR</li>
                            <li>2 medium skinless chicken drumsticks OR</li>
                            <li>1 piece of fish + 1 boiled egg (or 1 fruit serving)</li>
                            <li>2 boiled eggs + 1 small orange (or 1 plum) </li>
                            <li>Coffee + 200cal of creamer</li>
                        </ul><br>

                        <p>You can add the following to any of the options you choose above</p>
                        <ul>
                            <li>Cucumbers, carrots, garden eggs</li>
                            <li>You can have any zero-calorie drink along with the above options. Examples, Coke zero, Pepsi light, Sans cream soda, H20, Seven up light. Ask questions if you are not sure about the status of any drink</li>
